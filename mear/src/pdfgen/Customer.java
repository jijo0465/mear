package pdfgen;

public class Customer {
	private String customerProfileInvAddr;
	private String customerProfileProAddr;
	private String customerProfileEmail;
	private String customerProfileConPer;
	private String customerProfilePhone;
	private String name;
	private String trn;
	
	public String getTrn() {
		return trn;
	}
	public void setTrn(String trn) {
		this.trn = trn;
	}
	public String getCustomerProfileInvAddr() {
		return customerProfileInvAddr;
	}
	public void setCustomerProfileInvAddr(String customerProfileInvAddr) {
		this.customerProfileInvAddr = customerProfileInvAddr;
	}
	public String getCustomerProfileProAddr() {
		return customerProfileProAddr;
	}
	public void setCustomerProfileProAddr(String customerProfileProAddr) {
		this.customerProfileProAddr = customerProfileProAddr;
	}
	public String getCustomerProfileEmail() {
		return customerProfileEmail;
	}
	public void setCustomerProfileEmail(String customerProfileEmail) {
		this.customerProfileEmail = customerProfileEmail;
	}
	public String getCustomerProfileConPer() {
		return customerProfileConPer;
	}
	public void setCustomerProfileConPer(String customerProfileConPer) {
		this.customerProfileConPer = customerProfileConPer;
	}
	public String getCustomerProfilePhone() {
		return customerProfilePhone;
	}
	public void setCustomerProfilePhone(String customerProfilePhone) {
		this.customerProfilePhone = customerProfilePhone;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
